// Language translations class
class LanguageTranslator {
    constructor() {
        this.currentLanguage = localStorage.getItem('safeguard_language') || 'en';
        this.translations = translations;
    }
    
    // Get translation for a key
    t(key) {
        const langTranslations = this.translations[this.currentLanguage];
        if (langTranslations && langTranslations[key]) {
            return langTranslations[key];
        }
        // Fallback to English
        if (this.translations['en'][key]) {
            return this.translations['en'][key];
        }
        return key;
    }
    
    // Set language
    setLanguage(lang) {
        if (this.translations[lang]) {
            this.currentLanguage = lang;
            localStorage.setItem('safeguard_language', lang);
            this.updatePageTranslations();
            this.updateLanguageSelector();
            return true;
        }
        return false;
    }
    
    // Get current language
    getLanguage() {
        return this.currentLanguage;
    }
    
    // Update all elements with data-translate attribute
    updatePageTranslations() {
        const elements = document.querySelectorAll('[data-translate]');
        elements.forEach(el => {
            const key = el.getAttribute('data-translate');
            const translation = this.t(key);
            if (translation) {
                el.textContent = translation;
            }
        });
        
        // Update placeholders
        const placeholderElements = document.querySelectorAll('[data-translate-placeholder]');
        placeholderElements.forEach(el => {
            const key = el.getAttribute('data-translate-placeholder');
            const translation = this.t(key);
            if (translation) {
                el.placeholder = translation;
            }
        });
        
        // Dispatch custom event for other scripts to handle
        window.dispatchEvent(new CustomEvent('languageChanged', { 
            detail: { language: this.currentLanguage } 
        }));
    }
    
    // Update language selector UI
    updateLanguageSelector() {
        const selector = document.getElementById('languageSelector');
        if (selector) {
            selector.value = this.currentLanguage;
        }
        
        // Update all language buttons
        const buttons = document.querySelectorAll('.lang-btn');
        buttons.forEach(btn => {
            btn.classList.remove('active');
            if (btn.dataset.lang === this.currentLanguage) {
                btn.classList.add('active');
            }
        });
    }
    
    // Initialize language selector
    initLanguageSelector() {
        // Only run if DOM is ready
        if (document.readyState === 'loading') {
            document.addEventListener('DOMContentLoaded', () => {
                this.updatePageTranslations();
                this.updateLanguageSelector();
            });
        } else {
            this.updatePageTranslations();
            this.updateLanguageSelector();
        }
    }
}

// Create global instance and attach to window
window.translator = new LanguageTranslator();
window.translator.initLanguageSelector();

// Function to change language (called by buttons)
function changeLanguage(lang) {
    if (translator) {
        translator.setLanguage(lang);
    }
}

// Get translation for a key (global function)
function translate(key) {
    if (translator) {
        return translator.t(key);
    }
    return key;
}
